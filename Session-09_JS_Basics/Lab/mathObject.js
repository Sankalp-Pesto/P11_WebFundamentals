// find the absolute value of a number
let num1 = -5;
let absNum1 = Math.abs(num1);
console.log(absNum1); // Output: 5

// round a number to the nearest integer
let num2 = 3.7;
let roundNum2 = Math.round(num2);
console.log(roundNum2); // Output: 4

// find the largest number in a set of numbers
let num3 = 10;
let num4 = 5;
let maxNum = Math.max(num3, num4);
console.log(maxNum); // Output: 10

// generate a random number between 0 and 1
let randomNum = Math.random();
console.log(randomNum); // Output: a random decimal number between 0 and 1
