// get the length of a string
let str1 = "Hello, World!";
let len1 = str1.length;
console.log(len1); // Output: 13

// convert a string to uppercase
let str2 = "hello, world!";
let upperStr2 = str2.toUpperCase();
console.log(upperStr2); // Output: "HELLO, WORLD!"

// convert a string to lowercase
let str3 = "HELLO, WORLD!";
let lowerStr3 = str3.toLowerCase();
console.log(lowerStr3); // Output: "hello, world!"

// extract a substring from a string
let str4 = "JavaScript is awesome!";
let subStr4 = str4.substring(0, 10);
console.log(subStr4); // Output: "JavaScript"

// find the index of a character in a string
let str5 = "Hello, World!";
let indexStr5 = str5.indexOf("W");
console.log(indexStr5); // Output: 7
